      <h3>Les autobus :</h3>

      La TRANSTU assure la charge de transport des voyageurs à l’intérieur de la capitale et vers les banlieues. Elle dessert tous les cités et quartiers résidentiels.
      Les principales stations de la TRANSTU se concentrent dans la capitale :
      <ul>
          <li>Station du parc Thameur</li>
          <li>Station Ali Belahouane</li>
          <li> Station place Barcelone</li>
          <li> Station Tunis-Marine</li>
          <li>Station Avenue de Carthage</li>
      </ul>
      <h3>Le métro-léger :</h3>
      <p>Le métro léger est au centre du système de transport urbain à la ville. Il assure le transport des voyageurs dans les directions suivantes :</p>
      <ul>
    <li> Tunis-Marine - Place Barcelone - Ben Arous</li>
          <li> Tunis - Ariana</li>
          <li> Tunis - Cité Ettadhamen</li>
          <li>Tunis – Den Den</li>
          <li> Tunis – Cité Ibn Khaldoun</li>
    <li> Ligne Tunis-Marine - La Goulette - Sidi Bousaîd- La Marsa (TGM)</li>
    <p>Il est appelé à se développer dans la direction de la Manouba pour desservir le Campus universitaire
    et El Mourouj pour assurer le transport vers cette nouvelle grande cité.</p>
      </ul>

